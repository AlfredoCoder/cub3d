#include "libft.h"

char	*ft_strdup(const char *src)
{
	char	*string;
	int		src_size;
	int		i;

	i = 0;
	src_size = ft_strlen(src);
	string = (char *)malloc(sizeof (*string) * (src_size + 1));
	while (i < src_size)
	{
		string[i] = src[i];
		i++;
	}
	string[src_size] = '\0';
	return (string);
}
